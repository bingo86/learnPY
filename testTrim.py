def trim(s):
    if 0==len(s):       #判断输入字符串长度是否为0，如果为0立即返回S
        return s
    while ' '==s[0]:    #判断输入字符串前边是否为空，为空则将空格去掉，从不为空的位置取数值
        s=s[1:]
        if 0==len(s):   #去掉空格后再次判断输入内容长度是否为0，为0立即返回S
            return s
    while ' '==s[-1]:   #判断输入字符串尾部有没有空格，有则去掉然后将非空位置数值赋值给s
        s=s[:-1]
        if 0==len(s):   #判断输入字符串长度是否为0，为0立即返回s
            return s
    return s
